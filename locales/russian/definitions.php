function Gdn_UserBox(&$Author, $date = '', $check_html = '')
{
    $date_class = '';
    if($date) $date = Gdn_DateF($date, $date_class);
    $profile_url = Url('/profile/'.$Author->UserID.'/'.rawurlencode($Author->Name));
    $avatar = UserPhoto($Author);
    $avatar = $avatar ? $avatar : '<a href="/profile/'.$profile_url.'" title="'.htmlspecialchars($Author->Name).'"><img src="/uploads/stub-avatar.gif"></a>';
    echo'<table class="Author"><tr>
    ',($check_html ? '<td class="AdminCheck">'.$check_html.'</td>' : ''),'
    <td class="avatar">',$avatar,'</td>
    <td class="profile"><a href="',$profile_url,'">',htmlspecialchars($Author->Name),'</a></td>',($date ? '<td class="date'.$date_class.'"><span>'.$date.'</span></td>' : ''),'
    </tr></tbody></table>';
}